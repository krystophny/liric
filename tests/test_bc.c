static int run_jit_bc_api(const uint8_t *data, size_t len, const char *fname) {
    char err[512] = {0};
    lr_module_t *m = lr_parse_bc(data, len, err, sizeof(err));
    if (!m) {
        fprintf(stderr, "  BC parse error: %s\n", err);
        return -99999;
    }

    lr_jit_t *jit = lr_jit_create();
    if (!jit) {
        lr_module_free(m);
        return -99999;
    }

    if (lr_jit_add_module(jit, m) != 0) {
        lr_jit_destroy(jit);
        lr_module_free(m);
        return -99999;
    }

    typedef int (*fn_t)(void);
    fn_t fn;
    LR_JIT_GET_FN(fn, jit, fname);
    if (!fn) {
        lr_jit_destroy(jit);
        lr_module_free(m);
        return -99999;
    }

    {
        int out = fn();
        lr_jit_destroy(jit);
        lr_module_free(m);
        return out;
    }
}

