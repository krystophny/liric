int test_bc_module_constants_update_global_value_table(void) {
    lr_module_t *m;
    lr_func_t *bar = NULL;
    lr_block_t *b;
    lr_inst_t *call = NULL;
    const char *callee_name;
    char err[256] = {0};

    if (!lr_bc_parser_available())
        return 0;

    m = lr_parse_bc(bc_modconst_data, bc_modconst_len, err, sizeof(err));
    TEST_ASSERT(m != NULL, "BC parse with module constants succeeds");

    for (bar = m->first_func; bar; bar = bar->next) {
        if (strcmp(bar->name, "bar") == 0)
            break;
    }
    TEST_ASSERT(bar != NULL, "bar function found");
    TEST_ASSERT(!bar->is_decl, "bar is definition");

    for (b = bar->first_block; b && !call; b = b->next) {
        lr_inst_t *inst;
        for (inst = b->first; inst; inst = inst->next) {
            if (inst->op == LR_OP_CALL) {
                call = inst;
                break;
            }
        }
    }
    TEST_ASSERT(call != NULL, "call instruction found in bar");
    TEST_ASSERT(call->num_operands > 0, "call has callee operand");
    TEST_ASSERT(call->operands[0].kind == LR_VAL_GLOBAL, "callee is global symbol");

    callee_name = lr_module_symbol_name(m, call->operands[0].global_id);
    TEST_ASSERT(callee_name != NULL, "callee symbol resolves");
    TEST_ASSERT(strcmp(callee_name, "foo") == 0, "callee resolves to foo");

    lr_module_free(m);
    return 0;
}

typedef struct {
    uint32_t total;
    uint32_t ret_count;
    uint32_t br_count;
    uint32_t phi_count;
} bc_stream_stats_t;

static int bc_stream_collect_cb(lr_func_t *func, lr_block_t *block,
                                const lr_bc_inst_desc_t *inst, void *ctx) {
    bc_stream_stats_t *stats = (bc_stream_stats_t *)ctx;
    (void)func;
    (void)block;
    if (!stats || !inst)
        return -1;
    stats->total++;
    if (inst->op == LR_OP_RET)
        stats->ret_count++;
    else if (inst->op == LR_OP_BR || inst->op == LR_OP_CONDBR)
        stats->br_count++;
    else if (inst->op == LR_OP_PHI)
        stats->phi_count++;
    return 0;
}

typedef struct {
    uint32_t calls;
} bc_stream_abort_t;

static int bc_stream_abort_cb(lr_func_t *func, lr_block_t *block,
                              const lr_bc_inst_desc_t *inst, void *ctx) {
    bc_stream_abort_t *state = (bc_stream_abort_t *)ctx;
    (void)func;
    (void)block;
    (void)inst;
    if (!state)
        return -1;
    state->calls++;
    return 1;
}

